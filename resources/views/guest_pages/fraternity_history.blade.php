            <h1>Fraternity History</h1>

            <h3>National History</h3>

            <p>Phi Beta Kappa, the first American college fraternity, was organized on the campus of 
the College of William and Mary in 1776. Men and women are members of this college honorary 
scholarship society.</p>

            <p>Greek-lettered fraternities and sororities have played a major role in American 
college life since 1776. Black college fraternities and sororities did not emerge 
until the early 1900’s. Unlike their white counterparts, the black groups have 
remained very active at the graduate level. Since their founding, these groups have 
played a major role in the cultural, social and civic life of their communities.</p>

            <h3>The Birth of Omega</h3>

            <p>On Friday evening, November 17, 1911, three Howard University undergraduate students,
with the assistance of their faculty adviser, gave birth to the Omega Psi Phi 
Fraternity. This event occurred in the office of biology Professor Ernest E. Just, 
the faculty adviser, in the Science Hall (now known as Thirkield Hall). The three 
liberal arts students were Edgar A. Love, Oscar J. Cooper and Frank Coleman. From 
the initials of the Greek phrase meaning friendship is essential to the soul, the 
name Omega Psi Phi was derived. The phrase was selected as the motto. Manhood, 
scholarship, perseverance and uplift were adopted as cardinal principles. A 
decisions made regarding the design for the pin and emblem, and thus ended the 
first meeting of the Omega Psi Phi Fraternity . The next meeting was conducted on 
November 23, 1911. Edgar Love became the first Grand Basileus (National President). 
Cooper and Coleman were selected Grand keeper of the Records (National Secretary) 
and Grand keeper of Seals (National Treasurer), respectively. Eleven Howard 
University undergraduate men were selected as charter members. Alpha Chapter was 
organized with fourteen charter members on December 15, 1911. Love, Cooper and 
Coleman were elected the chapter’s first Basileus, Keeper of Records, and Keeper of 
Seals, respectively.</p>

            <p>On March 8, 1912, the previously submitted fraternity constitution was rejected by 
the Howard University Faculty Council. The Faculty Council proposed to accept the 
fraternity as a local but not a national organization. The fraternity refused 
acceptance as a strictly local organization. Oscar Cooper became the fraternity’s 
second Grand Basileus in 1912. Cooper authorized the investigation of a proposed 
second chapter at Lincoln University, Pennsylvania. Edgar Love was elected as the 
third Grand Basileus in 1912and served until 1915. In 1914, Howard University 
withdrew its opposition, and the Omega Psi Phi Fraternity was incorporated under 
the laws of the District of Columbia on October 28, 1914.</p>

            <p>Beta Chapter at Lincoln University was chartered in February, 1914. George E. Hall, 
the fourth Grand Basileus, had been initiated at Alpha Chapter in 1914. Grand 
Basileus Hall authorized the establishment of Gamma Chapter in Boston, Massachusetts. 
However, the chapter was eventually established during the administration of the 
fifth Grand Basileus, James C. McMorries. During the administration of the sixth 
Grand Basileus, Clarence F. Holmes, the fraternity’s first official hymn, Omega Men 
Draw Nigh, was written by Otto Bohannon. Raymond G. Robinson, the seventh Grand 
Basileus, established Delta Chapter in Nashville, Tennessee in 1919. Robinson left 
office in 1920 with a total of ten chapters in operation. Stanley Douglas served as 
Editor of the first Oracle published in the spring of 1919. Harold K. Thomas, the 
eighth Grand Basileus, was elected at the 1920 Nashville Grand Conclave. It was at 
this Conclave that Carter G. Woodson inspired the establishment of National 
Achievement Week to promote the study of Negro life and history. The 1921 Atlanta 
Grand Conclave brought to an end the first decade of the Omega Psi Phi Fraternity.</p>

            <h3>Internal Growth</h3>

            <p>In 1922, Grand Basileus J. Alston Atkins appointed the first District Representatives. 
Today, there are eleven such officers who are elected annually by the district 
conferences/meetings. In 1922, the office of Vice Grand Basileus was created. The 
Grand Keeper of Records became the Grand Keeper of the Records and Seal. The first 
Omega Bulletin was published in 1928. Campbell C. Johnson was the Editor. Omega Dear 
was adopted as the official hymn in 1931. Two faculty from Howard University, 
Charles R. Drew, Professor of Surgery, and Mercer Cook, Professor of Languages, 
were the composers. Cook wrote the music and first stanza; Drew wrote the last two 
stanzas.</p>

